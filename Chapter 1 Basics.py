# print returns the value from the parenthesis
# use quotes to mark the text
print("Hello")

# there is no need to use quotes on numbers
print(5)

# Python runs the file line by line
# We can print two or more things at once also, by using a comma
print("There was a cat", 34)

# We can also perform calculations!
print(4+3)  # Add
print(3-1)  # Subtract
print(3*6)  # Multiply
print(10/5) # Division

# We can add a seperator too.
# Default seperator is space 
print("Hello", "Nice", "WEll", 4, 233, sep="%")

# We can end with a character in a print statement also
print("Nothing", "But", end="@")
