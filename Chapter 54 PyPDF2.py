# There is an exellent module existing in python which is used to merge pdfs and extract text/images from a single pdf
# The module is 'PyPDF2'
# You need to install it by writing 'pip install PyPDF2' in your terminal.

import PyPDF2       # Importing module

a = PyPDF2.PdfWriter()      # Creating an object

# To create pdf
a.write("create.pdf") 


